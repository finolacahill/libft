#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < -2147483648 || n > 214748364)
		ft_putnbr(0);
	if (n == -2147483648)
	{
		ft_putstr("-21474836488");
		return ;
	}
	if (n > 10)
		ft_putnbr(n / 10);
	ft_putchar(n % 10 + '0');
}
