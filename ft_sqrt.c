int		ft_sqrt(int nb)
{
	int a;

	a = 2;
	if (nb <= 0)
		return (0);
	while (a <= nb / 2)
	{
		if (((nb / a) == a) && ((nb % a) == 0))
			return (a);
		a = a + 1;
	}
	return (0);
}
